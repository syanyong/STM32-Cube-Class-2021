/* USER CODE BEGIN 0 */
void sensor_init(){
    for(int i = 0 ; i<8 ; i++)
    {
  	  sensors_min[i]=4095;  // 12 bits
  	  sensors_max[i]=0;
    }
}
void setup_sensor(){
	while(HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_13) == 0)
	{
		for(int i =0;i<8;i++)
		{
		  if(sensors_max[i]<adc_current[i])
		  {
		  	sensors_max[i] = adc_current[i];
		  }
		  if(sensors_min[i]>adc_current[i])
		  {
		  	sensors_min[i] = adc_current[i];
		  }
		}
	}
}
void normalize_sensor(){
	sum_norm_ss=0;
	for(int i =0;i<8;i++)
	{
		norm_ss[i] = 1-(((float)adc_current[i]-(float)sensors_min[i])/((float)sensors_max[i]-(float)sensors_min[i]));
		sum_norm_ss += norm_ss[i];
	}
}
void get_position(){
	position = ((float)(-50*norm_ss[0])+(float)(-35.72*norm_ss[1])+(float)(-21.44*norm_ss[2])+(float)(-7.16*norm_ss[3])+(float)(7.16*norm_ss[4])+(float)(21.4*norm_ss[5])+(float)(35.72*norm_ss[6])+(float)(50*norm_ss[7]))/(float)(sum_norm_ss);
}
